#include <stdio.h>
int main() {
    int num;
    printf("Enter an integer: ");
    scanf("%d", &num);

    printf("%d << 4 = %d\n", num, num << 4);

    printf("%d << 3 = %d\n", num, num << 3);

    printf("%d >> 5 = %d\n", num, num >> 5);

    printf("%d >> 2 = %d\n", num, num >> 2);

    return 0;
}
